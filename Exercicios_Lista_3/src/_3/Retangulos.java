package _3;

public class Retangulos extends Quadrilateros {

	public Retangulos(int lado1, int lado2, int lado3, int lado4) {
		super();
		this.setLado1(lado1);
		this.setLado2(lado2);
		this.setLado3(lado3);
		this.setLado4(lado4);

	}

	public double calculaArea() {
		int lados;

		return lados = getLado1() * getLado2();

	}

}
